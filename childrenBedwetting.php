<br>
<li>Medical Conditions must be checked for such as bladder infections or kidney problems can be possible causes.</li>
<br>
<li>Deep sleep or sleep issues could prevent your child from noticing and failing to respond to a full bladder.  It could also be that their body has got into a habit of relaxing all the muscles in deep sleep so that the child doesn’t notice the signals.</li>
