          Challenging Our Thoughts - Helping Our Children To Understand Theirs<br><br>
The saying ‘what we focus on, we become’ certainly has some truth! Our thoughts left unchecked can lead us down some very dark paths. It can lead to:
<ul>
<li> Self-doubt</li>
<br>
<li>Procrastination</li>
<br>
<li>Short Attention Span</li>
<br>
<li>Pessimism</li>
<br>
<li>Isolation</li>
<br>
<li>Anxiety</li>
<br>
<li>Depression</li>
<br>
<li>Anger</li>
<br>
<li>Low academic/sporting/work performance.</li>
<br>
<li>Poor Choices and Decisions</li>
<br>
</ul>
If we as adults struggle to keep our thoughts under control, imagine the challenges our children and teenagers face.
<br><br>
We need to support them to have the courage and confidence to take on new challenges and build their ability to bounce back when setbacks happen.
<br><br>
We do this best by modelling to them how it is done. Our children will adopt what we do long before they do as we say.
<br><br>
If we are unable to self-regulate and control our emotional reactions to our thoughts our tamariki are more likely to find it challenging as well.
<br><br>
So how do we begin to get our thoughts under some kind of control especially when our lives and theirs are so busy and often overwhelming?
<br><br>
The challenge here is not to try and get rid of these thoughts but to be able to manage them when they do arise.
<br><br>
We need to first catch them! That is the challenge!
<br><br>
<h4>As adults we can challenge our thoughts:</h4>
<ul>
<li> Check is the thought we are thinking, true? Or just a story we are telling ourselves?</li>
<br>
<li> Can I think about this differently?</li>
<br>
<li> Is there something for me to learn?</li>
<br>
<li> Is this thought taking me towards what I want or moving me away from it?</li>
<br>
<li> Am I focused on the whole picture or just a small part of it?</li>
</ul>
<strong>To support our children and teenagers we must:</strong>
<br>
<ul>
<li> Make it okay to talk about our/their feelings</li>
<br>
<li> Let your child know that these thoughts and feelings are okay in fact normal…we all have them!</li>
<br>
<li> Allow them the time and space to talk with you, away from the hustle and bustle of the day. A walk, or sitting outside in the garden, away from distraction and devices.</li>
<br>
<li> Support your child/teenager to take small positive steps towards a solution rather than have them give up or walk away from what is worrying them.</li>
<br>
<li> Your understanding, support and time builds a child’s inner strength and self-belief. If they know you believe in them, they are more likely to believe in themselves.</li>
</ul>
Most importantly, be patient with yourself and with your child.❤
