We are creatures of habit and we develop habits for everything.  Children often develop ways to soothe or self settle and most children move on from these in their own time as they begin to become more confident and develop better skills to support themselves.  
<br><br>
However some children find it more challenging and over time find themselves left with a habit that can be challenging to let go of.  
<br><br>
A habit is formed through repetition, whether it is a thought or a behavior, the more frequently we engage in it the more automatic it becomes.  
<br><br>
Overtime we no longer need to consciously perform the behavior, as we create such a deeply embedded loop that it becomes unconscious, no longer requiring thought. So, as soon as we encounter a trigger, the behavior automatically begins, even when the person no longer wants it to. 
<br><br>
The habit is initiated by a ‘trigger’, this could be driven by time (bedtime, bathtime), people, places or events and a habit may often have multiple triggers. The trigger automatically drives the ‘behaviour’ (thumb-sucking, nail-biting etc) which is now a habit and is unconscious. Your child may well be unaware that they are doing it. The reward is often a feeling, calm, relaxed, safe or maybe it just feels familiar which is comforting especially for children.
